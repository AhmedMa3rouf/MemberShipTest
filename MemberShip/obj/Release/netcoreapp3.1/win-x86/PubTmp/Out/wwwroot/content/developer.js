function MoreAndLess(showChar, selector, moretext, lesstext) {
    // Configure/customize these variables.
    var ellipsestext = "...";

    var content = $(selector).html();
    if (content.length > showChar) {

        var c = content.substr(0, showChar);
        var h = content.substr(showChar, content.length - showChar);

        var html = c + '<span class="moreellipses">' + ellipsestext + '&nbsp;</span><span class="morelesscontent"><span>' + h + '</span>&nbsp;&nbsp;<a href="" class="lnkMoreLess" style="color:#0595B1;">' + moretext + '</a></span>';

        $(selector).html(html);
    }

    $(selector + " .lnkMoreLess").click(function () {
        if ($(this).hasClass("less")) {
            $(this).removeClass("less");
            $(this).html(moretext);
        } else {
            $(this).addClass("less");
            $(this).html(lesstext);
        }
        $(this).parent().prev().toggle();
        $(this).prev().toggle();
        return false;
    });
}

